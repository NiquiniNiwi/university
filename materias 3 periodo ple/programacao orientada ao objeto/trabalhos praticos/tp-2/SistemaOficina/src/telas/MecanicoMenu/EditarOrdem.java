    //Botao para confirmar o recebimento de um servico e abrir a proxima tela
    private void confirmServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmServicoActionPerformed
        // TODO add your handling code here:
        //Torna a tela atual invisivel de forma a "limpar" a tela
        if(verificaCampoInt(inputServico) == true){
            this.setVisible(false);
            
            //Chama o construtor de FormatarServicos e torna a tela visivel, esta sendo passado o input do campo de texto, no entanto ele esta sendo convertido para int
            new FormatarServicos(this, Integer.parseInt(inputServico.getText()), this.mecani).setVisible(true);
        }
    }//GEN-LAST:event_confirmServicoActionPerformed
    
