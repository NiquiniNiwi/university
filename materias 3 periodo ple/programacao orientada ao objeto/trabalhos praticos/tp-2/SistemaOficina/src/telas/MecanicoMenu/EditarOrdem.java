    public boolean verificaCampoInt(JTextField text){
        int aux;
        try{
            aux = Integer.parseInt(text.getText());
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Campo invalido", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

