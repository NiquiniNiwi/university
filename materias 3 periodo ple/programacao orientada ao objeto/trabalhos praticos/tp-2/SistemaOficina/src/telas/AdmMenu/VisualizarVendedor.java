    //Listar informações de um vendedor em forma de lista dinâmica
    public void showVendedores(){
        
        vendedorX = adm.getVendedor(idVend);
        
        JLabel labelNome = new javax.swing.JLabel();        
        labelNome.setVisible(true);
        labelNome.setText("Nome:  " + vendedorX.getNome());
        labelNome.setSize(300,40);
        labelNome.setLocation(50, space);
        getContentPane().add(labelNome);
        
        
        space+=20;
        JLabel labelCpf = new javax.swing.JLabel();   
        labelCpf.setVisible(true);
        labelCpf.setText("Cpf:  " + vendedorX.getCpf());
        labelCpf.setSize(300,40);
        labelCpf.setLocation(50, space);
        getContentPane().add(labelCpf);
        
        space+=20;
        JLabel labelRg = new javax.swing.JLabel(); 
        labelRg.setVisible(true);
        labelRg.setText("Rg:  " + vendedorX.getRg());
        labelRg.setSize(300,40);
        labelRg.setLocation(50, space);
        getContentPane().add(labelRg);
        
        space+=20;
        JLabel labelEndereco = new javax.swing.JLabel(); 
        labelEndereco.setVisible(true);
        labelEndereco.setText("Endereco:  " + vendedorX.getEndereco());
        labelEndereco.setSize(300,40);
        labelEndereco.setLocation(50, space);
        getContentPane().add(labelEndereco);
        
        space+=20;
        JLabel labelTelefone = new javax.swing.JLabel(); 
        labelTelefone.setVisible(true);
        labelTelefone.setText("Telefone:  " + vendedorX.getTelefone());
        labelTelefone.setSize(300,40);
        labelTelefone.setLocation(50, space);
        getContentPane().add(labelTelefone);
        
        space+=20;
        JLabel labelEmail = new javax.swing.JLabel(); 
        labelEmail.setVisible(true);
        labelEmail.setText("Email:  " + vendedorX.getEmail());
        labelEmail.setSize(300,40);
        labelEmail.setLocation(50, space);
        getContentPane().add(labelEmail);
                
        initComponents();
    }

