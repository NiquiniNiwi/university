    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
        //Criando um novo Vendedor e o inserindo na lista
        if(!(InputNome.getText()).equals("") && !(InputCpf.getText()).equals("") && !(InputRg.getText()).equals("") && !(InputEndereco.getText()).equals("") && !(InputTelefone.getText()).equals("") && !(InputSenha.getText()).equals("") && !(InputEmail.getText()).equals("")){
            Vendedor vendedor = new Vendedor(InputNome.getText(), InputCpf.getText(), InputRg.getText(), InputEndereco.getText(), InputTelefone.getText(), InputEmail.getText(), InputUser.getText(), InputSenha.getText());
            adm.addVendedor(vendedor);
            menu_adm.setAdminstrador(adm);
            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso", "Message", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);    
            menu_adm.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null, "Campos vazios n√£o s√£o permitidos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_CadastrarActionPerformed

