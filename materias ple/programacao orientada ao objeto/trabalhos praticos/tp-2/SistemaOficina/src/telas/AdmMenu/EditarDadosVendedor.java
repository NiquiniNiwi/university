    public void editInputs() {
        vendedorX = adm.getVendedor(idVend);
        
        InputNome.setText(vendedorX.getNome());
        InputEndereco.setText(vendedorX.getEndereco());
        InputTelefone.setText(vendedorX.getTelefone());
        InputEmail.setText(vendedorX.getEmail());
        InputUser.setText(vendedorX.getUser());
        InputSenha.setText(vendedorX.getSenha());
    }

