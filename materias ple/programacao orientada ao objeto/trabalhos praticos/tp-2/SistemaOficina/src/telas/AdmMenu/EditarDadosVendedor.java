    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed
        //Atualizando as informações do Vendedor em questão
        if(!(InputNome.getText()).equals("") && !(InputEndereco.getText()).equals("") && !(InputTelefone.getText()).equals("") && !(InputSenha.getText()).equals("") && !(InputEmail.getText()).equals("")){
            adm.setVendedor(new Vendedor(InputNome.getText(), vendedorX.getCpf(), vendedorX.getRg(), InputEndereco.getText(), InputTelefone.getText(), InputEmail.getText(), InputUser.getText(), InputSenha.getText()));
            alterar.recebeAdm(adm);
            JOptionPane.showMessageDialog(null, "Salvo com sucesso", "Message", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);   
            alterar.setVisible(true);
            alterar.showVendedores();
        }
        else{
            JOptionPane.showMessageDialog(null, "Campos vazios não são permitidos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_SalvarActionPerformed

